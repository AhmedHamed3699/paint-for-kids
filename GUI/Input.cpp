#include "Input.h"
#include "Output.h"


Input::Input(window* pW)
{
	pWind = pW; //point to the passed window
}

void Input::GetPointClicked(int& x, int& y) const
{
	pWind->WaitMouseClick(x, y);	//Wait for mouse click
}

string Input::GetSrting(Output* pO) const
{
	string Label;
	char Key;
	while (1)
	{
		pWind->WaitKeyPress(Key);
		if (Key == 27)	//ESCAPE key is pressed
			return "";	//returns nothing as user has cancelled label
		if (Key == 13)	//ENTER key is pressed
			return Label;
		if ((Key == 8) && (Label.size() >= 1))	//BackSpace is pressed
			Label.resize(Label.size() - 1);
		else
			Label += Key;
		if (pO)
			pO->PrintMessage(Label);
	}
}

//This function reads the position where the user clicks to determine the desired action
ActionType Input::GetUserAction() const
{
	int x, y;
	pWind->WaitMouseClick(x, y);	//Get the coordinates of the user click

	if (UI.InterfaceMode == MODE_DRAW)	//GUI in the DRAW mode
	{
		//[1] If user clicks on the Toolbar
		if (y >= 0 && y < UI.ToolBarHeight)
		{
			//Check whick Menu item was clicked
			//==> This assumes that menu items are lined up horizontally <==
			int ClickedItemOrder = (x / UI.MenuItemWidth);
			//Divide x coord of the point clicked by the menu item width (int division)
			//if division result is 0 ==> first item is clicked, if 1 ==> 2nd item and so on

			switch (ClickedItemOrder)
			{
			case ITM_EXIT:			  return EXIT;
			case ITM_ADD_FIGURE:	  return ADD_FIGURE;
			case ITM_SELECT_ONE:	  return SELECT;
			case ITM_DRAWING_COLOR:   return CHOOSE_DRAWING_COLOR;
			case ITM_FILL_COLOR:      return CHANGE_FILL_COLOR;
			case ITM_DELETE_FIGURE:   return DELETE_FIGURE;
			case ITM_MOVE_FIGURE:	  return MOVE_FIGURE;
			case ITM_UNDO:			  return UNDO;
			case ITM_REDO:			  return REDO;
			case ITM_CLEAR_ALL:		  return CLEAR_ALL;
			case ITM_START_RECORDING: return START_RECORDING;
			case ITM_STOP_RECORDING:  return STOP_RECORDING;
			case ITM_PLAY_RECORDING:  return PLAY_RECORDING;
			case ITM_SAVE:			  return SAVE;
			case ITM_LOAD:			  return LOAD;
			case ITM_SWITCH:		  return TO_PLAY;
			default: return EMPTY;	//A click on empty place in desgin toolbar
			}
		}

		//[2] User clicks on the drawing area
		if (y >= UI.ToolBarHeight && y < UI.height - UI.StatusBarHeight)
		{
			return DRAWING_AREA;
		}

		//[3] User clicks on the status bar
		return STATUS;
	}
	else if (UI.InterfaceMode == MODE_SHAPES) //GUI in the Shapes mode
	{
		//[1] If user clicks on the Toolbar
		if (y >= 0 && y < UI.ToolBarHeight)
		{
			//Check whick Menu item was clicked
			//==> This assumes that menu items are lined up horizontally <==
			int ClickedItemOrder = (x / UI.MenuItemWidth);
			//Divide x coord of the point clicked by the menu item width (int division)
			//if division result is 0 ==> first item is clicked, if 1 ==> 2nd item and so on

			switch (ClickedItemOrder)
			{
			case ITM_RECT:	 return DRAW_RECT;
			case ITM_SQUARE: return DRAW_SQUARE;
			case ITM_CIRCLE: return DRAW_CIRCLE;
			case ITM_TRI:    return DRAW_TRI;
			case ITM_HEX:    return DRAW_HEX;
			default: return EMPTY;	//A click on empty place in desgin toolbar
			}
		}

		//[2] User clicks on the drawing area
		if (y >= UI.ToolBarHeight && y < UI.height - UI.StatusBarHeight)
		{
			return DRAWING_AREA;
		}

		//[3] User clicks on the status bar
		return STATUS;
	}
	else if (UI.InterfaceMode == MODE_COLORS) //GUI in the Colors mode
	{
		//[1] If user clicks on the Toolbar
		if (y >= 0 && y < UI.ToolBarHeight)
		{
			//Check whick Menu item was clicked
			//==> This assumes that menu items are lined up horizontally <==
			int ClickedItemOrder = (x / UI.MenuItemWidth);
			//Divide x coord of the point clicked by the menu item width (int division)
			//if division result is 0 ==> first item is clicked, if 1 ==> 2nd item and so on

			switch (ClickedItemOrder)
			{
			case ITM_BLACK:	 return COLOUR_BLACK;
			case ITM_YELLOW: return COLOUR_YELLOW;
			case ITM_ORANGE: return COLOUR_ORANGE;
			case ITM_RED:    return COLOUR_RED;
			case ITM_GREEN:  return COLOUR_GREEN;
			case ITM_BLUE:	 return COLOUR_BLUE;
			default: return EMPTY;	//A click on empty place in desgin toolbar
			}
		}

		//[2] User clicks on the drawing area
		if (y >= UI.ToolBarHeight && y < UI.height - UI.StatusBarHeight)
		{
			return DRAWING_AREA;
		}

		//[3] User clicks on the status bar
		return STATUS;
	}
	else	//GUI is in PLAY mode
	{
		//[1] If user clicks on the Toolbar
		if (y >= 0 && y < UI.ToolBarHeight)
		{
			//Check whick Menu item was clicked
			//==> This assumes that menu items are lined up horizontally <==
			int ClickedItemOrder = (x / UI.MenuItemWidth);
			//Divide x coord of the point clicked by the menu item width (int division)
			//if division result is 0 ==> first item is clicked, if 1 ==> 2nd item and so on

			switch (ClickedItemOrder)
			{
			case ITM_PICK_BY_SHAPES: return PICK_BY_SHAPES;
			case ITM_PICK_BY_COLORS: return PICK_BY_COLORS;
			case ITM_PICK_BY_BOTH:	 return PICK_BY_BOTH;
			case ITM_DRAW_MODE:      return TO_DRAW;
			case ITM_PLAY_EXIT:		 return EXIT;
			default: return EMPTY;	//A click on empty place in desgin toolbar
			}
		}

		//[2] User clicks on the drawing area
		if (y >= UI.ToolBarHeight && y < UI.height - UI.StatusBarHeight)
		{
			return DRAWING_AREA;
		}

		//[3] User clicks on the status bar
		return STATUS;
	}

}
